package com.company.blocks;

public class DataBlock {
    private int word1;
    private int word2;
    private int word3;
    private int word4;

    public DataBlock(int word1, int word2, int word3, int word4) {
        this.word1 = word1;
        this.word2 = word2;
        this.word3 = word3;
        this.word4 = word4;
    }

    public int getWord1() {
        return word1;
    }

    public int getWord2() {
        return word2;
    }

    public int getWord3() {
        return word3;
    }

    public int getWord4() {
        return word4;
    }
}
